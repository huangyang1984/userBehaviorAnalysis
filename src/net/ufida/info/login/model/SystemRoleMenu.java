package net.ufida.info.login.model;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

import net.ufida.x27.util.web.JSONNotAware;

/**
 * 系统角色菜单关联类
 */
public class SystemRoleMenu implements JSONNotAware, Serializable, Cloneable {
	
	private int subsys_role_id;
	
	private int subsys_menu_id;
	
	private List sysRoleList = new ArrayList(0);
	
	private List sysMenuList = new ArrayList(0);
	//以下是getter和setter方法

	public int getSubsys_role_id() {
		return subsys_role_id;
	}

	public void setSubsys_role_id(int subsysRoleId) {
		subsys_role_id = subsysRoleId;
	}

	public int getSubsys_menu_id() {
		return subsys_menu_id;
	}

	public void setSubsys_menu_id(int subsysMenuId) {
		subsys_menu_id = subsysMenuId;
	}

	public List getSysRoleList() {
		return sysRoleList;
	}

	public void setSysRoleList(List sysRoleList) {
		this.sysRoleList = sysRoleList;
	}

	public List getSysMenuList() {
		return sysMenuList;
	}

	public void setSysMenuList(List sysMenuList) {
		this.sysMenuList = sysMenuList;
	}
	
}
