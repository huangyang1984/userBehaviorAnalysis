package net.ufida.info.util;

import java.util.List;

/**
 * @author Steven.yang
 * String 工具类
 */
public class StringUtil {
	 public static void copyByAdd(List src,List dest){
         for (int i = 0 ; i< src.size() ;i++){
             Object obj = src.get(i);
             dest.add(obj);
         }
     } 

}
